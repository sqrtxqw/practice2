#Python Conditions and If statements
a = 33
b = 200
if b > a:
  print("b is greater than a")

#The Elif Keyword
a = 33
b = 33
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")

#The Else Keyword
a = 200
b = 33
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")
else:
  print("a is greater than b")

#Short Hand If
a = 2
b = 330
print("A") if a > b else print("B")

